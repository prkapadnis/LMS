    <h4>
        A library management information system can also be called a library management system (LMS) or integrated
        library
        system (ILS). It is a system that makes use of information technology (IT) to carry out managerial objectives.
        The
        main goal of a library management information system is to store, organize, share and retrieve vital information
        needed to carry out daily operational functions of the library.
    </h4>
    <h4> <b> Benefit </b> </h4>
    <h4>
    LMS makes everyday library tasks more efficient. This means more work can be done in less time. Consequently, this
    decreases operational costs. This also minimizes paperwork and manual tasks, thus allowing library personnel to
    concentrate on other things such as interaction with users.
